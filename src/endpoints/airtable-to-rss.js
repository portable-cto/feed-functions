const express = require('express');
const router = new express.Router();
const fetch = require('node-fetch');
const {Feed} = require('feed');
const querystring = require('querystring');
const logger = require("logops");

router.get('/', function (req, res, next) {
  if (!(
    req.query.api_key &&
    req.query.baseId &&
    req.query.table &&
    req.query.view
  )) {
    next('Missing required parameter(s). Please check: api_key, baseId, table, view');
  }

  // Get items from Airtable
  const airtableUrl = 'https://api.airtable.com/v0/' +
    req.query.baseId + '/' +
    req.query.table + '?' +
    querystring.stringify({
      api_key: req.query.api_key,
      maxRecords: '20',
      view: req.query.view,
      sort: req.query.sort || [{field: "created", direction: "desc"}]
    });
  const fullUrl = req.protocol + '://' + req.get('host') + req.originalUrl;

  const feed = new Feed({
    id: fullUrl,
    title: 'Airtable to RSS',
    description: 'RSS Feed Generated by Airtable',
    generator: 'https://functions.portablecto.com/airtable-to-rss',
    link: fullUrl,
  });

  fetch(airtableUrl)
    .then((raw => raw.json()))
    .then(results => {
      results.records.forEach(record => {
        feed.addItem({
          title: record.fields.title,
          id: record.fields.url,
          link: record.fields.url,
          description: record.fields.description,
          author: [{name: record.fields.name}],
          date: new Date(record.fields.created),
          image: record.fields.image ? record.fields.image[0].url : undefined,
        });
      });

      res.writeHead(200, {'Content-Type': 'application/xml'});
      res.end(feed.rss2());
    }).catch(error => {
      logger.error(error);
      next(error);
    });
});

module.exports = router;
